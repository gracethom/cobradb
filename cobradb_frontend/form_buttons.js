/* Show new person form */

function newPerson() {
    var $b = $('button.newPerson');
    var $f = $('.personForm');
    var $p = $('.personPrompt');
    
    $b.click(function () {
        var i = $b.index(this);
        $f.hide().eq(i).show()
        $p.hide()
    })
};

